// @title           Smart Document Intelligence API
// @version         1.0.0
// @description     Async document processing API with AI-powered extraction, classification, and semantic search.
// @description     Upload PDF, PNG, JPG, or TXT documents and get structured data extracted automatically.

// @contact.name    Indra Styawan
// @contact.url     https://github.com/IndraSty
// @contact.email   your-email@example.com

// @license.name    MIT
// @license.url     https://opensource.org/licenses/MIT

// @host            localhost:8080
// @BasePath        /

// @securityDefinitions.apikey BearerAuth
// @in              header
// @name            Authorization
// @description     JWT access token. Format: Bearer {token}

// @securityDefinitions.apikey ApiKeyAuth
// @in              header
// @name            X-API-Key
// @description     SHA-256 hashed API key obtained at registration

// @tag.name        auth
// @tag.description User registration and authentication

// @tag.name        documents
// @tag.description Document upload, retrieval, and management

// @tag.name        search
// @tag.description Semantic, full-text, and hybrid document search

// @tag.name        system
// @tag.description Health check and system status

func main() {
	// ── Bootstrap ────────────────────────────────────────────────────
	log := logger.New("development")
	log.Info().Msg("Starting Smart Document Intelligence API...")

	cfg, err := config.Load()
	if err != nil {
		log.Fatal("Failed to load configuration", err)
	}

	log = logger.New(cfg.App.Env)
	log = log.WithService("api")

	ctx := context.Background()

	// ── Migrations ───────────────────────────────────────────────────
	m, err := migrate.New("file://migrations", cfg.Database.URL)
	if err != nil {
		log.Fatal("Failed to initialize migrations", err)
	}
	if err := m.Up(); err != nil && err != migrate.ErrNoChange {
		log.Fatal("Failed to run migrations", err)
	}
	log.Info().Msg("Database migrations applied")

	// ── Database ─────────────────────────────────────────────────────
	pool, err := database.NewPostgresPool(ctx, &cfg.Database, log)
	if err != nil {
		log.Fatal("Failed to connect to PostgreSQL", err)
	}
	defer pool.Close()

	// ── Redis ────────────────────────────────────────────────────────
	redisClient, err := redisrepo.NewRedisClient(cfg.Redis.URL, log)
	if err != nil {
		log.Fatal("Failed to connect to Redis", err)
	}
	defer func() { _ = redisClient.Close() }()

	// ── RabbitMQ ─────────────────────────────────────────────────────
	queueClient, err := queue.NewClient(&cfg.RabbitMQ, log)
	if err != nil {
		log.Fatal("Failed to connect to RabbitMQ", err)
	}
	defer queueClient.Close()

	// ── Storage ──────────────────────────────────────────────────────
	storageClient := storage.NewClient(&cfg.Supabase, log)

	// ── AI ───────────────────────────────────────────────────────────
	aiClient, err := gemini.NewClient(ctx, &cfg.Gemini, log)
	if err != nil {
		log.Fatal("Failed to initialize Gemini client", err)
	}

	embedder, err := embedding.NewGenerator(ctx, &cfg.Gemini, log)
	if err != nil {
		log.Fatal("Failed to initialize embedding generator", err)
	}
	defer func() { _ = embedder.Close() }()

	// ── Repositories ─────────────────────────────────────────────────
	userRepo := postgres.NewUserRepository(pool, log)
	docRepo := postgres.NewDocumentRepository(pool, log)
	extractionRepo := postgres.NewExtractionRepository(pool, log)
	jobRepo := redisrepo.NewJobRepository(redisClient, log)

	// ── Usecases ─────────────────────────────────────────────────────
	processingUC := usecase.NewProcessingUsecase(jobRepo, docRepo, queueClient, log)
	userUC := usecase.NewUserUsecase(userRepo, cfg, log)
	documentUC := usecase.NewDocumentUsecase(docRepo, jobRepo, processingUC, storageClient, cfg, log)
	searchUC := usecase.NewSearchUsecase(extractionRepo, docRepo, embedder, log)

	// Suppress unused variable warning for aiClient until worker stage
	_ = aiClient

	// ── Middleware ───────────────────────────────────────────────────
	authMW := middleware.NewAuthMiddleware(userRepo, cfg, log)
	rateLimiter := middleware.NewRateLimiter(&cfg.RateLimit, log)

	// ── Handlers ─────────────────────────────────────────────────────
	authHandler := handler.NewAuthHandler(userUC, log)
	documentHandler := handler.NewDocumentHandler(documentUC, log)
	searchHandler := handler.NewSearchHandler(searchUC, log)
	healthHandler := handler.NewHealthHandler(pool, redisClient, queueClient, storageClient, log)

	// ── Router ───────────────────────────────────────────────────────
	router := deliveryhttp.NewRouter(deliveryhttp.RouterDeps{
		AuthHandler:     authHandler,
		DocumentHandler: documentHandler,
		SearchHandler:   searchHandler,
		HealthHandler:   healthHandler,
		AuthMiddleware:  authMW,
		RateLimiter:     rateLimiter,
		Config:          cfg,
		Log:             log,
	})

	// Swagger UI — available at /swagger/index.html
	router.GET("/swagger/*", echoSwagger.WrapHandler)

	// ── Graceful shutdown ────────────────────────────────────────────
	go func() {
		port := cfg.App.Port
		if port == "" {
			port = "8080"
		}
		log.Info().Str("port", port).Msg("API server listening")
		if err := router.Start(":" + port); err != nil {
			log.Info().Msg("Server stopped")
		}
	}()

	// Wait for interrupt signal
	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)
	<-quit

	log.Info().Msg("Shutting down gracefully...")

	shutdownCtx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
	defer cancel()

	if err := router.Shutdown(shutdownCtx); err != nil {
		log.Error().Err(err).Msg("Server forced to shutdown")
	}

	log.Info().Msg("Server exited cleanly")
}
